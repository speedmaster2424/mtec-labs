
import Foundation

struct MorseCodeCharacter {
    let signals: [MorseCodeSignal]
    let character: Character
        
    init?(character: Character) {
        guard let pattern = characterToPatternDictionary[character.lowercased()] else {
            return nil
        }

        self.signals = pattern.map {
            MorseCodeSignal(rawValue: String($0))!
        }
        self.character = character
    }    
}

private let characterToPatternDictionary: [String: String] = [
    "0": "-----",
    "1": ".----",
    "2": "..---",
    "3": "...--",
    "4": "....-",
    "5": ".....",
    "6": "-....",
    "7": "--...",
    "8": "---..",
